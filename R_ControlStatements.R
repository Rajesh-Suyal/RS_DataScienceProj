### Example 1 Condtional statements ####

##1. If Statement##
x = -3
if (x < 1 & x != 0){   
  print("x is a negative number")
}

##2. If and else Statement##
y = 5
if(y < 0 & y !=0){
  print("y is negative number")
}else {
  print("y is positive number")
}

##3. If and else if statement##

z = 0
if(z > 0 & z !=0){
  print("z is positive number")
}else if(z < 0 & z !=0){
  print("z is negative number")
}else {
  print("z is equal to zero")
}